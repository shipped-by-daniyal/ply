#!/usr/bin/env node
// Regenerates the WHOLE-FILE-GENERATED token reference under
// apps/docs/src/content/docs/tokens/ from packages/tokens/src/*.tokens.json (ADR-0009).
// No Figma dependency — CI verifies freshness with `git diff --exit-code` on the output dir.
// Idempotent: same src → byte-identical output. Run via /ply-update-docs or directly:
//   node scripts/gen-token-docs.mjs
import { readFileSync, writeFileSync, mkdirSync, readdirSync, rmSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SRC = path.join(ROOT, "packages/tokens/src");
const OUT = path.join(ROOT, "apps/docs/src/content/docs/tokens");
const load = (f) => JSON.parse(readFileSync(path.join(SRC, f), "utf8"));

const primitives = load("primitives.tokens.json");
const brand1 = load("brand.brand-1.tokens.json");
const brand2 = load("brand.brand-2.tokens.json");
const light = load("semantic.light.tokens.json");
const dark = load("semantic.dark.tokens.json");
const fontGeist = load("font.geist.tokens.json");
const fontDga = load("font.die-grotesk-a.tokens.json");

const flat = (obj, prefix = []) => {
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    if (v && typeof v === "object" && "$value" in v) out[[...prefix, k].join("/")] = v;
    else if (v && typeof v === "object") Object.assign(out, flat(v, [...prefix, k]));
  }
  return out;
};
const P = flat(primitives), B1 = flat(brand1), B2 = flat(brand2), L = flat(light), D = flat(dark);

// resolve a $value to a concrete value; brand refs resolve through Brand 1 unless brandMap given
const resolve = (v, modeMap, brandMap = B1) => {
  let cur = v;
  const seen = new Set();
  while (typeof cur === "string" && cur.startsWith("{")) {
    const ref = cur.slice(1, -1).replaceAll(".", "/");
    if (seen.has(ref)) throw new Error("alias cycle at " + ref);
    seen.add(ref);
    const t = P[ref] ?? brandMap[ref] ?? modeMap[ref];
    if (!t) throw new Error("unresolved ref " + cur);
    cur = t.$value;
  }
  return cur;
};
// pinned to sd.config.mjs's name/ply transform — change one, change both (ADR-0009)
const cssVar = (p) => ("--ply-" + p.replaceAll("/", "-")).toLowerCase();
const refName = (v) => (typeof v === "string" && v.startsWith("{") ? v.slice(1, -1).replaceAll(".", "/") : null);
const esc = (s) => String(s).replaceAll("|", "\\|").replaceAll("\n", " ");

const BANNER = `{/* GENERATED — do not hand-edit. Regenerated by scripts/gen-token-docs.mjs via /ply-update-docs. */}`;
const page = (title, description, order, body) =>
  `---\ntitle: "${title}"\ndescription: "${description}"\nsidebar:\n  order: ${order}\n---\n\n${BANNER}\n\nimport TokenSwatch from "../../../components/TokenSwatch.astro";\n\n${body}\n`;

const files = {};
const covered = new Set();

// ---- semantic property pages ----
const GROUPS = [
  ["background", "Backgrounds", "Surface, neutral, brand, status, selected, inverse, and static fills."],
  ["text", "Text", "Text colors: neutral, brand, link, status, static, disabled, selected."],
  ["icon", "Icons", "Icon colors mirror the text roles glyph-for-glyph."],
  ["border", "Borders", "Borders and the focus ring."],
  ["data-viz", "Data visualization", "Categorical series colors for charts, avatars, and stage indicators."],
  ["effect", "Effects", "Shadow layer tints consumed by the shadow effect styles."],
];
for (const [group, title, blurb] of GROUPS) {
  const rows = Object.keys(L)
    .filter((p) => p.startsWith(group + "/"))
    .sort()
    .map((p) => {
      covered.add(p);
      const lv = resolve(L[p].$value, L);
      const dv = resolve(D[p].$value, D);
      const lRef = refName(L[p].$value), dRef = refName(D[p].$value);
      const desc = L[p].$description ?? "";
      return `| <TokenSwatch light="${lv}" dark="${dv}" /> | \`${p}\` | \`${cssVar(p)}\` | ${esc(lRef ?? lv)} | ${esc(dRef ?? dv)} | ${esc(desc)} |`;
    });
  const body = [
    blurb,
    "",
    "| | Token | CSS variable | Light | Dark | Description |",
    "|---|---|---|---|---|---|",
    ...rows,
  ].join("\n");
  files[`${group === "data-viz" ? "data-viz" : group}.mdx`] = page(title, `Ply ${title.toLowerCase()} tokens — generated reference.`, GROUPS.findIndex((g) => g[0] === group) + 3, body);
}

// ---- primitives page (color ramps + brand ramp) ----
{
  const hues = {};
  for (const [p, t] of Object.entries(P)) {
    if (t.$type !== "color") continue;
    const hue = p.split("/")[0];
    (hues[hue] ??= []).push([p, t.$value]);
  }
  const sections = Object.keys(hues)
    .sort()
    .map((hue) => {
      const rows = hues[hue].map(([p, v]) => `| <TokenSwatch light="${v}" /> | \`${p}\` | \`${v}\` |`);
      return [`### ${hue}`, "", "| | Primitive | Value |", "|---|---|---|", ...rows].join("\n");
    });
  const brandRows = Object.keys(B1)
    .sort((a, b) => (a.endsWith("950") ? 1 : b.endsWith("950") ? -1 : Number(a.split("/")[1]) - Number(b.split("/")[1])))
    .map((p) => {
      const v1 = resolve(B1[p].$value, {}), v2 = resolve(B2[p].$value, {}, B2);
      return `| <TokenSwatch light="${v1}" dark="${v2}" /> | \`${p}\` | \`${cssVar(p)}\` | ${esc(refName(B1[p].$value))} | ${esc(refName(B2[p].$value))} |`;
    });
  const body = [
    "Tier-1 ramps. **Never consume these directly** — always go through a semantic token. They are not emitted to CSS (the brand ramp is the one exception: it is the live brand axis).",
    "",
    "## Brand ramp (colors-brand)",
    "",
    "Swatches show Brand 1 (left) and Brand 2 (right). Switch brands in code with `data-brand=\"brand-2\"` on a root element.",
    "",
    "| | Token | CSS variable | Brand 1 | Brand 2 |",
    "|---|---|---|---|---|",
    ...brandRows,
    "",
    "## Color ramps",
    "",
    ...sections,
  ].join("\n");
  files["primitives.mdx"] = page("Primitives", "Tier-1 color ramps and the brand axis — generated reference.", 9, body);
}

// ---- scales page ----
{
  const sections = [];
  const scaleOf = (predicate, title, note) => {
    const rows = Object.entries(P)
      .filter(([p, t]) => predicate(p, t))
      .map(([p, t]) => `| \`${p}\` | \`${cssVar(p)}\` | ${esc(Array.isArray(t.$value) ? t.$value.join(", ") : t.$value)} |`);
    if (rows.length) sections.push([`## ${title}`, "", ...(note ? [note, ""] : []), "| Token | CSS variable | Value |", "|---|---|---|", ...rows].join("\n"));
  };
  scaleOf((p) => p.startsWith("space/"), "Space", "Gapped base-4 scale (0–176px).");
  scaleOf((p) => p.startsWith("radius/"), "Radius");
  scaleOf((p) => p.startsWith("font-size-"), "Font size");
  scaleOf((p) => p.startsWith("weight-"), "Font weight");
  scaleOf((p) => p.startsWith("line-height-"), "Line height");
  scaleOf((p) => p.startsWith("breakpoint/"), "Breakpoints");
  scaleOf((p, t) => t.$type === "fontFamily", "Font families", `Default mode is Inter. Alternate modes via \`data-font="geist"\` or \`data-font="die-grotesk-a"\`: font-sans becomes ${resolve(flat(fontGeist)["font-sans"].$value, {})} / ${resolve(flat(fontDga)["font-sans"].$value, {})}; font-mono stays IBM Plex Mono.`);
  files["scales.mdx"] = page("Scales & typography", "Space, radius, type scales, breakpoints, and font families — generated reference.", 10, sections.join("\n\n"));
}

// ---- index ----
{
  const semCount = Object.keys(L).length;
  const groupCounts = GROUPS.map(([g, title]) => `- [${title}](/tokens/${g}/) — ${Object.keys(L).filter((p) => p.startsWith(g + "/")).length} tokens`);
  const body = [
    `Ply's tokens mirror the reference library verbatim ([ADR-0007](/decisions/adr-0007-adopt-reference-naming/)): **${Object.keys(P).filter((p) => primitivesIsColor(p)).length} color primitives**, **${Object.keys(B1).length} brand steps**, and **${semCount} semantic tokens** in Light + Dark. Values flow Figma → DTCG JSON → CSS custom properties + TypeScript.`,
    "",
    "## Reference",
    "",
    ...groupCounts,
    `- [Primitives](/tokens/primitives/) — raw ramps (never consumed directly)`,
    `- [Scales & typography](/tokens/scales/) — space, radius, type, breakpoints, fonts`,
    "",
    "## Consuming tokens",
    "",
    "```css",
    "/* CSS: import the built stylesheet, then use the custom properties */",
    '@import "@ply/tokens/dist/css/ply.css";',
    ".card { background: var(--ply-background-surface-flat-default); }",
    "```",
    "",
    "```ts",
    "// TypeScript: typed paths + a cssVar helper",
    'import { tokens, cssVar } from "@ply/tokens/dist/ts/tokens";',
    'cssVar("background.surface.flat.default"); // "var(--ply-background-surface-flat-default)"',
    "```",
    "",
    "Theming axes: `data-theme=\"dark\"` (mode), `data-brand=\"brand-2\"` (brand ramp), `data-font=\"geist\" | \"die-grotesk-a\"` (type).",
    "",
    "Grammar: nested role paths (`background/neutral/subtle/hovered`), a six-rung emphasis ladder (`subtlest → boldest`), explicit `/default` leaves, and a `status/` sub-namespace. Nine legacy `deprecated/*` variables exist in Figma for binding stability only — they never reach code.",
    "",
    "_Contrast contract: every documented background pair passes 4.5:1 in both modes — enforced by `scripts/lint-tokens.mjs` in CI._",
  ].join("\n");
  function primitivesIsColor(p) { return P[p].$type === "color"; }
  files["index.mdx"] = page("Tokens", "The Ply token reference — generated from the DTCG source of truth.", 1, body);
}

// ---- coverage assertion ----
const missed = Object.keys(L).filter((p) => !covered.has(p));
if (missed.length) throw new Error("tokens on no page: " + missed.join(", "));

// ---- write (full-directory ownership: remove stale generated files) ----
mkdirSync(OUT, { recursive: true });
for (const existing of readdirSync(OUT)) {
  if (!(existing in files)) rmSync(path.join(OUT, existing));
}
for (const [name, content] of Object.entries(files)) writeFileSync(path.join(OUT, name), content);
console.log(`gen-token-docs: ${Object.keys(files).length} pages, ${Object.keys(L).length} semantic tokens covered → ${OUT}`);
